L = [8, 24, 27, 48, 2, 16, 9, 102, 7, 84, 91]

# Récupérer la valeur, le maximum et le minimum des éléments de la liste
valeur = L[3]  # Par exemple, récupérer la valeur à l'indice 3 (48 dans cet exemple)
valeur_maximale = max(L)
valeur_minimale = min(L)


print("La valeur à l'indice 3 est :", valeur)
print("La valeur maximale de la liste est :", valeur_maximale)
print("La valeur minimale de la liste est :", valeur_minimale)
